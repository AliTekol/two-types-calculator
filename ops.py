import math

def addition(a,b):
    return float(a)+float(b)

def subtraction(a,b):
    return float(a)-float(b)

def multiplier(a,b):
    return float(a)*float(b)

def division(a,b):
    return float(a)/float(b)

def sine(a):
    return float(math.sin(a))

def cosine(a):
    return float(math.cos(a))

def tangent(a):
    return float(math.tan(a))

def cotangent(a):
    return float(1/math.tan(a))

def square_root(a):
    return float(math.sqrt(a))

def power(a,b):
    return float(math.pow(a,b))

def factorial(a):
    return float(math.factorial(a))

def logarithm(a,b):
    return float(math.log(a,b))